package com.javaex.problem01;

public class Mul {
	private int a;
	private int b;

	public Mul() {

	}

	public Mul(String a, String b) {
		this.a = Integer.parseInt(a);
		this.b = Integer.parseInt(b);
	}

	public int getA() {
		return a;
	}

	public int getB() {
		return b;
	}

}
